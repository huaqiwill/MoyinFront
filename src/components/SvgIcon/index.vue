<template>
  <svg aria-hidden="true" class="icon">
    <use :href="symbolId" :fill="color"/>
  </svg>
</template>

<style lang="scss" scoped>
.icon {
  width: 32px;
  height: 32px;
}
</style>

<script lang="ts">
import {defineComponent, computed} from 'vue'

export default defineComponent({
  name: 'SvgIcon',
  props: {
    prefix: {
      type: String,
      default: 'icon',
    },
    name: {
      type: String,
      required: true,
    },
    color: {
      type: String,
      default: '#333',
    },
  },
  setup(props) {
    const symbolId = computed(() => `#${props.prefix}-${props.name}`)
    return {symbolId}
  },
})
</script>
